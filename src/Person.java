 class Person{
    public int ID;
    public String FullName;
    public String Login;
    public int Password;
    public String CardNumber;
    public int CVV;
    public double Balance;

    public Person(int id,String fullName , String login, int password, String cardNumber, int cvv, double balance) {this.ID=id;
       this.FullName=fullName;
       this.Login=login;
       this.Password=password;
       this.CardNumber=cardNumber;
       this.CVV=cvv;
       this.Balance=balance;
    }

 }
